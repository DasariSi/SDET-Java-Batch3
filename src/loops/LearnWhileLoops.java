package loops;

public class LearnWhileLoops {

	public static void main(String[] args) {

		int x = 11;  //initialisation

		while (x <= 10) { //conditions check
			System.out.println(x);
			x = x + 1; //increment or decrement
		}

		
		x=1; //initialisation
		
		do { //run for atleast 1 iteration
			System.out.println(x);
		} while (x<1); //condition check
		
		
		
	}

}
